/**
 * App-wide Material 3 theme. Dark-first: the hand-tuned teal-green schemes above are the
 * brand identity, with the dark scheme designed to sit next to the live terminal surface.
 *
 * On Android 12+ [dynamicColor] (default ON) switches to the wallpaper-derived Material You
 * palette; pass `dynamicColor = false` to force the built-in terminal identity. Below
 * Android 12 the built-in schemes are always used.
 */
@Composable
fun HomeAttachTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    dynamicColor: Boolean = false,
    content: @Composable () -> Unit,
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }

        darkTheme -> DarkColors
        else -> LightColors
    }
    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content,
    )
}
